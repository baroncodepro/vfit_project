"""
vfit/export/spice_export.py
───────────────────────────
Export a FosterNetwork (or RationalModel) as a SPICE subcircuit netlist.

Two export modes are available:

1. **Foster ladder** (`mode='foster'`):
   Writes a standard R / L / C netlist from the synthesised element values.
   Requires running `foster_synthesis(model)` first.

2. **Behavioral** (`mode='behavioral'`):
   Writes a Laplace-domain B-source using the rational transfer function
   directly.  Supported by HSPICE, LTspice (with .laplace), Spectre.

Usage
-----
    from vfit.export.spice_export import export_spice_foster, export_spice_behavioral
    export_spice_foster(network, "my_model.cir")
    export_spice_behavioral(model, "my_model_beh.cir")
"""

from __future__ import annotations

import textwrap
from pathlib import Path

import numpy as np

from ..core.rational_function import RationalModel
from ..rlc.rlc_synthesis import FosterNetwork, RLCBranch


