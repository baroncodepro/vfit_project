def _write_file(path: Path, lines: list[str]) -> None:
    """Write a list of strings to a file, one line each."""
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def export_spice_foster(
    network: FosterNetwork,
    path: str | Path,
    subckt_name: str = "VF_FOSTER",
    port_pos: str = "1",
    port_neg: str = "0",
) -> None:
    """
    Write a SPICE subcircuit for the Foster-I RLC ladder.

    Branches are connected in series (port_pos → Z1 → Z2 → ... → port_neg)
    using internal nodes n0, n1, ...  Each branch element (R/L/C) is placed
    in parallel between the two nodes of that branch segment.

    Parameters
    ----------
    network : FosterNetwork
        Synthesised RLC network from foster_synthesis().
    path : str or Path
        Output file path (e.g. 'model.cir').
    subckt_name : str
        SPICE subcircuit name.
    port_pos, port_neg : str
        Positive and negative port node names.
    """
    path = Path(path)
    lines: list[str] = []

    lines.append(f"* Vector Fitting — Foster-I RLC Subcircuit")
    lines.append(f"* Generated by vfit/export/spice_export.py")
    if network.model is not None:
        m = network.model
        lines.append(f"* Poles: {m.n_poles}   RMS error: {m.rms_error:.3e}")
    lines.append("")
    lines.append(f".SUBCKT {subckt_name} {port_pos} {port_neg}")
    lines.append("")

    elem_counter = {"R": 0, "L": 0, "C": 0}
    branches = network.branches
    n_branches = len(branches)

    # Build a series chain of internal nodes:
    #   port_pos → [branch 0] → n0 → [branch 1] → n1 → ... → port_neg
    def node_name(i: int) -> str:
        """Node after branch i (before branch i+1)."""
        return port_neg if i == n_branches - 1 else f"n{i:04d}"

    prev_node = port_pos
    for idx, branch in enumerate(branches):
        next_node = node_name(idx)
        _write_branch(lines, branch, idx, prev_node, next_node, elem_counter)
        lines.append("")
        prev_node = next_node

    lines.append(f".ENDS {subckt_name}")
    lines.append("")

    _write_file(path, lines)
    print(f"[vfit] SPICE Foster netlist written → {path}")


def _write_branch(
    lines: list[str],
    branch: RLCBranch,
    idx: int,
    node_a: str,
    node_b: str,
    ctr: dict,
) -> None:
    """Append SPICE element lines for one branch."""
    bt = branch.branch_type
    mid = f"nm{idx}"    # internal mid-node when two elements needed

    def next_R() -> str:
        name = f"R{ctr['R']:04d}"
        ctr['R'] += 1
        return name

    def next_L() -> str:
        name = f"L{ctr['L']:04d}"
        ctr['L'] += 1
        return name

    def next_C() -> str:
        name = f"C{ctr['C']:04d}"
        ctr['C'] += 1
        return name

    if bt == "R_only":
        lines.append(f"* Branch {idx}: series resistance (d-term)")
        lines.append(f"{next_R()}   {node_a}  {node_b}  {_fmt(branch.R)}")

    elif bt == "C_only":
        lines.append(f"* Branch {idx}: shunt capacitance (near-DC pole)")
        lines.append(f"{next_C()}   {node_a}  {node_b}  {_fmt(branch.C)}")

    elif bt == "L_only":
        lines.append(f"* Branch {idx}: series inductance (e-term)")
        lines.append(f"{next_L()}   {node_a}  {node_b}  {_fmt(branch.L)}")

    elif bt == "RL":
        # Legacy parallel RL (should not appear in new synthesis)
        # Z = sLR/(sL+R):  R and L in parallel between port nodes
        lines.append(f"* Branch {idx}: parallel RL (legacy)  pole={branch.pole:.4e}")
        lines.append(f"{next_R()}  {node_a} {node_b}  {_fmt(branch.R)}")
        lines.append(f"{next_L()}  {node_a} {node_b}  {_fmt(branch.L)}")

    elif bt == "RC":
        # Parallel RC: Z = R/(1+sRC)
        # Correct topology for a real LHP pole p=-alpha with residue c:
        #   R = c/alpha,   C = 1/c
        # In SPICE: R and C in parallel between port nodes
        lines.append(f"* Branch {idx}: parallel RC  pole={branch.pole:.4e}")
        lines.append(f"* Z = R/(1+sRC),  R={_fmt(branch.R)},  C={_fmt(branch.C)}")
        lines.append(f"{next_R()}  {node_a} {node_b}  {_fmt(branch.R)}")
        lines.append(f"{next_C()}  {node_a} {node_b}  {_fmt(branch.C)}")

    elif bt == "RLC":
        # Parallel RLC: Z = 1/(1/R + 1/(sL) + sC)
        # All three elements in parallel between port nodes
        lines.append(f"* Branch {idx}: parallel RLC  pole={branch.pole:.4e}")
        lines.append(f"* Z = 1/(1/R + 1/sL + sC),  R={_fmt(branch.R)},  L={_fmt(branch.L)},  C={_fmt(branch.C)}")
        lines.append(f"{next_R()}  {node_a} {node_b}  {_fmt(branch.R)}")
        lines.append(f"{next_L()}  {node_a} {node_b}  {_fmt(branch.L)}")
        lines.append(f"{next_C()}  {node_a} {node_b}  {_fmt(branch.C)}")


