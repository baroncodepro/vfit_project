# Behavioral (Laplace) Export
# ─────────────────────────────────────────────────────────────────────────────

def export_spice_behavioral(
    model: RationalModel,
    path: str | Path,
    subckt_name: str = "VF_LAPLACE",
    port_in: str = "in",
    port_out: str = "out",
    port_gnd: str = "0",
) -> None:
    """
    Write an LTspice Laplace behavioral subcircuit for the rational model.

    Uses LTspice E-source LAPLACE syntax::

        E1 out 0 LAPLACE {V(in,0)} = {NUM(s)/DEN(s)}

    where NUM and DEN are polynomials in ascending powers of s.

    The subcircuit has three terminals: in, out, gnd (ground reference).
    Connect as: X1 <in_node> <out_node> 0 <subckt_name>

    Parameters
    ----------
    model : RationalModel
    path : str or Path
    subckt_name : str
    port_in, port_out, port_gnd : str
        Port node names inside the subcircuit definition.
    """
    path = Path(path)

    num_poly, den_poly = _model_to_poly(model)

    # Ascending-power coefficients (a0 + a1*s + a2*s^2 + ...)
    num_coeffs = num_poly[::-1]
    den_coeffs = den_poly[::-1]

    # Normalize by the geometric mean of pole magnitudes so that the
    # polynomial variable sn = s/w0 is of order 1 across the band.
    # Without normalization, s^4 at GHz frequencies reaches ~1e36, causing
    # catastrophic cancellation between terms and wrong LTspice results.
    w0 = _normalization_frequency(model)
    norm_num = np.array([c * w0**i for i, c in enumerate(num_coeffs)])
    norm_den = np.array([c * w0**i for i, c in enumerate(den_coeffs)])

    num_expr = _poly_expr_normalized(norm_num, w0)
    den_expr = _poly_expr_normalized(norm_den, w0)

    lines = [
        f"* Vector Fitting \u2014 Behavioral Laplace Subcircuit (LTspice)",
        f"* Generated by vfit/export/spice_export.py",
        f"* Poles: {model.n_poles}   RMS error: {model.rms_error:.3e}",
        f"* Normalization: w0 = {w0:.6e} rad/s  (sn = s/w0 is order-1 across band)",
        f"",
        f".SUBCKT {subckt_name} {port_in} {port_out} {port_gnd}",
        f"* V({port_out},{port_gnd}) = H(s) * V({port_in},{port_gnd})",
        f"* H(s) = NUM(sn) / DEN(sn),  sn = s / w0",
        f"E1 {port_out} {port_gnd} LAPLACE {{V({port_in},{port_gnd})}} = "
        f"{{({num_expr})/({den_expr})}}",
        f"Rbleed {port_out} {port_gnd} 1G   ; DC path \u2014 prevents floating node warning",
        f"",
        f".ENDS {subckt_name}",
        f"",
    ]

    _write_file(path, lines)
    print(f"[vfit] SPICE behavioral netlist written → {path}")


