# Helpers
# ─────────────────────────────────────────────────────────────────────────────

def _model_to_poly(model: RationalModel):
    """Return (num_poly, den_poly) as numpy coefficient arrays (descending)."""
    poles = model.poles
    residues = model.residues
    n = len(poles)

    den_poly = np.poly(poles).real   # shape (n+1,)

    # Numerator = d * den + Σᵢ cᵢ * Π_{j≠i}(s-pⱼ)
    num_poly = model.d * den_poly.copy()
    for i in range(n):
        other = np.delete(poles, i)
        partial = np.poly(other).real   # shape (n,)
        num_poly[1:] += residues[i].real * partial

    return num_poly, den_poly


def _fmt(val: float) -> str:
    """Format a float in SPICE-friendly scientific notation."""
    if val == 0:
        return "0"
    exp = int(np.floor(np.log10(abs(val))))
    mantissa = val / 10**exp
    # Use SPICE suffix if possible
    suffixes = {12: "T", 9: "G", 6: "MEG", 3: "K",
                0: "", -3: "m", -6: "u", -9: "n", -12: "p", -15: "f"}
    for e, suffix in sorted(suffixes.items(), reverse=True):
        if exp >= e:
            scaled = val / 10**e
            return f"{scaled:.4g}{suffix}"
    return f"{val:.4e}"


def num_str_inline(coeffs: np.ndarray) -> str:
    """Build inline polynomial string for LTspice: a0 + a1*s + a2*s^2 ..."""
    terms = []
    for i, c in enumerate(coeffs.real):
        if abs(c) < 1e-30:
            continue
        if i == 0:
            terms.append(f"{c:.4e}")
        elif i == 1:
            terms.append(f"{c:.4e}*s")
        else:
            terms.append(f"{c:.4e}*s**{i}")
    return " + ".join(terms) if terms else "0"


def _poly_expr(coeffs: np.ndarray) -> str:
    """
    Build a compact LTspice polynomial expression (ascending powers of s).

    Uses the form: c0 + c1*s + c2*s**2 + ...
    Empty terms (|c| < 1e-30) are omitted.
    """
    terms = []
    for i, c in enumerate(coeffs.real):
        if abs(c) < 1e-30:
            continue
        if i == 0:
            terms.append(f"{c:.6e}")
        elif i == 1:
            terms.append(f"{c:.6e}*s")
        else:
            terms.append(f"{c:.6e}*s**{i}")
    return "+".join(terms) if terms else "0"


def _fmt_hz(freq_hz: float) -> str:
    """Format a frequency in Hz as a compact LTspice string (e.g. 1e6 → '1Meg')."""
    if freq_hz >= 1e9:
        v = freq_hz / 1e9
        return f"{v:g}G"
    if freq_hz >= 1e6:
        v = freq_hz / 1e6
        return f"{v:g}Meg"
    if freq_hz >= 1e3:
        v = freq_hz / 1e3
        return f"{v:g}K"
    return f"{freq_hz:g}"


def _normalization_frequency(model: RationalModel) -> float:
    """Geometric mean of pole magnitudes — used to normalize s in LAPLACE export.

    Ensures all polynomial coefficients are order-1, avoiding the catastrophic
    cancellation that occurs when s^n at GHz reaches 1e30+ in double precision.
    """
    mags = np.abs(model.poles)
    return float(np.sqrt(mags.min() * mags.max()))


def _poly_expr_normalized(norm_coeffs: np.ndarray, w0: float) -> str:
    """Build LTspice polynomial in the normalized variable sn = s/w0.

    norm_coeffs[i] = original_coeffs[i] * w0**i, so these are order-1.
    The expression uses (s/<w0>) so LTspice evaluates with sn in-place.
    """
    w0_str = f"{w0:.6e}"
    terms = []
    threshold = max(abs(norm_coeffs.real)) * 1e-14  # relative threshold
    for i, c in enumerate(norm_coeffs.real):
        if abs(c) < threshold:
            continue
        if i == 0:
            terms.append(f"{c:.6e}")
        elif i == 1:
            terms.append(f"{c:.6e}*(s/{w0_str})")
        else:
            terms.append(f"{c:.6e}*(s/{w0_str})**{i}")
    return "+".join(terms) if terms else "0"


def export_spice_test_foster(
    network: FosterNetwork,
    path: str | Path,
    subckt_name: str = "VF_FOSTER",
    subckt_file: str | Path | None = None,
    freq_start_hz: float = 1e6,
    freq_stop_hz: float = 10e9,
) -> None:
    """
    Write a complete LTspice test circuit for the Foster RLC subcircuit.

    Includes AC analysis setup to measure impedance Bode plot.

    Parameters
    ----------
    network : FosterNetwork
    path : str or Path
        Output test circuit file path.
    subckt_name : str
        Name of the subcircuit to instantiate.
    subckt_file : str or Path, optional
        Path to the subcircuit file to include. If None, assumes subcircuit is defined inline.
    freq_start_hz, freq_stop_hz : float
        AC sweep frequency range in Hz (default: 1 MHz – 10 GHz).
    """
    path = Path(path)
    lines: list[str] = []

    f_start = _fmt_hz(freq_start_hz)
    f_stop  = _fmt_hz(freq_stop_hz)

    lines.append("* LTspice test circuit for Foster RLC subcircuit")
    lines.append("* Generated by vfit/export/spice_export.py")
    lines.append("* Run simulation and plot V(1) in Bode format to see impedance Z(s)")
    lines.append("")

    if subckt_file is not None:
        lines.append(f".include {Path(subckt_file).name}")
        lines.append("")

    lines.append("* Voltage source: 1V AC at node 1")
    lines.append("Vin  1 0 AC 1")
    lines.append("")
    lines.append("* Instantiate the subcircuit (port_pos=1, port_neg=0)")
    lines.append(f"X1 1 0 {subckt_name}")
    lines.append("")
    lines.append(f"* AC sweep: 100 points per decade from {f_start} to {f_stop}")
    lines.append(f".ac dec 100 {f_start} {f_stop}")
    lines.append("")
    lines.append(".end")

    _write_file(path, lines)
    print(f"[vfit] LTspice test circuit written → {path}")


